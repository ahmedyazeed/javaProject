package ReplitsP1;

import java.util.Scanner;

public class RepForloop5 {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);


        System.out.println( "Please enter a number");
        int a= sc.nextInt();
        for (int i = 0; i < a; i++) {
            System.out.println(i);

        }


    }
}
